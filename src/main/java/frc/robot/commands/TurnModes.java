package frc.robot.commands;
// 4 Possible Modes
// Combination of Direction and "Relative and Absolute"
// Relative moves in relation to the current angle
// Absolute moves in relation to a constant on the field "YET TO BE DETERMINED"
// Direction is turning left/right.
public enum TurnModes {
    COUNTER_CLOCKWISE_ABSOLUTE, COUNTER_CLOCKWISE_RELATIVE, CLOCKWISE_ABSOLUTE, CLOCKWISE_RELATIVE
}
